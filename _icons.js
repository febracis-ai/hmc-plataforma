/* ────────────────────────────────────────────────────────────────
   Sidebar collapse — persiste em localStorage ('hmc.sidebar')
   Trigger:  <button data-sidebar-toggle>...</button>
   Mobile:   <button data-sidebar-mobile-toggle>...</button>
   ──────────────────────────────────────────────────────────────── */
(function () {
  const KEY = 'hmc.sidebar';

  function apply() {
    const collapsed = localStorage.getItem(KEY) === 'collapsed';
    document.body.classList.toggle('sidebar-collapsed', collapsed);
  }

  function toggle() {
    const collapsed = !document.body.classList.contains('sidebar-collapsed');
    localStorage.setItem(KEY, collapsed ? 'collapsed' : 'expanded');
    apply();
  }

  function toggleMobile() {
    document.body.classList.toggle('sidebar-mobile-open');
  }

  function bind() {
    document.querySelectorAll('[data-sidebar-toggle]').forEach(el => {
      el.addEventListener('click', toggle);
    });
    document.querySelectorAll('[data-sidebar-mobile-toggle]').forEach(el => {
      el.addEventListener('click', toggleMobile);
    });
  }

  apply();
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', bind);
  } else {
    bind();
  }

  window.HMC_SIDEBAR = { toggle, toggleMobile };
})();
